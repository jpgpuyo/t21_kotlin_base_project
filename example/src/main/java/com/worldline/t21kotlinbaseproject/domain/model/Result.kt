package com.worldline.t21kotlinbaseproject.domain.model

import java.lang.Exception

/**
 * A generic class that holds a success value and exception.
 * @param <T>
 */
sealed class Result<out R> {

    data class Success<out T>(val data: T) : Result<T>()
    data class Error(val exception: Exception) : Result<Nothing>()

    override fun toString(): String {
        return when (this) {
            is Success<*> -> "Success[data=$data]"
            is Error -> "Error[exception=$exception]"
        }
    }
}

/**
 * `true` if [Result] is of type [Success] & holds non-null [Success.data].
 */
val Result<*>.succeeded
    get() = this is Result.Success && data != null

/**
 * Custom Exceptions
 */

class ServerErrorException(message: String = "Server error exception") : Exception(message)
class UserNotFoundException(message: String = "User not found exception") : Exception(message)

